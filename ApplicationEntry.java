/**
 * HotelBookingApp.java
 * 
 * This class represents the entry point of the Hotel Booking application.
 * It demonstrates how a Java program starts execution and prints output
 * to the console.
 * 
 * The application displays a welcome message along with the application
 * name and version.
 * 
 * @author Md Nayaj Mondal
 * @version 1.0
 */

public class HotelBookingApp {

    /**
     * Main method - Entry point of the Java application.
     * The JVM starts execution from this method.
     *
     * @param args Command-line arguments (not used here)
     */
    public static void main(String[] args) {

        // Display welcome message
        System.out.println("Welcome to Hotel Booking System");

        // Display application name and version
        System.out.println("Application: Hotel Booking System");
        System.out.println("Version: v1.0");

        // Indicate program termination
        System.out.println("Application started successfully. Exiting now...");
    }
}
